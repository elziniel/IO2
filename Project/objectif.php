<div id="juste">
	<p>
		Notre association œuvre dans le domaine de la sensibilisation et de la mobilisation sur l’enjeu climatique.
		Créée en mars 2007, elle est formée d’un collège de citoyens et d’un collège d’associations impliquées dans les questions d’énergie, de transports doux et de protection de l’environnement.
		Nous poursuivons deux objectifs :
	</p>
	<p>
		- Sensibiliser les citoyens à la thématique du dérèglement climatique et les accompagner dans une démarche de réduction de leurs émissions de gaz à effet de serre.
	</p>
	<p>
		- Accompagner les décideurs dans leurs choix au quotidien et leur stratégie de développement d’actions sur la question du dérèglement climatique.
	</p>
